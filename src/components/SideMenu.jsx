import { useUser } from "@clerk/clerk-react";
import { User } from "lucide-react";
import { SIDE_MENU_DATA } from "../assets/data";
import { useNavigate } from "react-router-dom";

const SideMenu = ({activeMenu}) => {
    const {user} = useUser();
    const navigate =useNavigate();
    return (
        <div className="w-64 h-[calc(100vh-61px)] bg-shite border-gray-200/50 p-5 sticky top-[61px] z-20">
            <div className="flex flex-col items-center justify-center gap-3 mt-3 mb-7">
                {user?.imageUrl ?(
                    <img src={user.imageUrl} alt={user.fullName} className="w-20 h-20 bg-slate-400 rounded-full object-cover"/>
                ): (
                    <User className="w-20 h-20 text-xl"/>
                )}
                <h5 className="text-gray-950 font-medium leading-6">
                {user?.fullName || ""}  
            </h5>
            </div>
            
            {SIDE_MENU_DATA.map((item, index)=>(
                <button key={`menu_${index}`}
                className={`flex items-center gap-3 w-full text-[15px] py-3 px-6 rounded-lg mb-3 hover:bg-gray-100 transition-all duration-200 cursor-pointer ${activeMenu === item.label ? "bg-purple-500 text-white font-medium shadow-md hover:bg-purple-600" : "hover:bg-gray-100 text-gray-700"}`}
                onClick={()=> navigate(item.path)}
               >
                    <item.icon className="text-xl"/>
                    {item.label}
                </button>
            ))}

        </div>

    )
}

export default SideMenu;